// for loop

console.log("used for loop");

for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// while loop

console.log("used while loop");

let j = 1;

while (j <= 5) {
  console.log(j);
  j++;
}

// // do while loop

console.log("used do while loop");

let k = 1;
do {
  console.log(k);
  k++;
} while (k <= 5);

